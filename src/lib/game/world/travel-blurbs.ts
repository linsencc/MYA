export function pickTravelArrivalBlurb(locationId: string, salt: number): string {
  const list = BLURBS[String(locationId ?? "").trim()];
  if (!list?.length) return "你已来到新地点。光线与回声随之改变；若她也在，会先抬眼确认你的出现。";
  const i = Math.abs(Math.floor(salt)) % list.length;
  return list[i]!;
}
